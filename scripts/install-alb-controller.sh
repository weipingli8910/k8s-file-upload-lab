  --policy-name AWSLoadBalancerControllerIAMPolicy \
  --policy-document file:///tmp/alb-controller-policy.json \
  --query 'Policy.Arn' --output text 2>/dev/null || \
  aws iam list-policies --query 'Policies[?PolicyName==`AWSLoadBalancerControllerIAMPolicy`].Arn' --output text)

echo "Policy ARN: $POLICY_ARN"

# Get OIDC provider
OIDC_PROVIDER=$(aws eks describe-cluster --name $CLUSTER_NAME --query "cluster.identity.oidc.issuer" --output text | sed -e "s/^https:\/\///")

# Try to get IAM role ARN from Terraform output
cd "$(dirname "$0")/../infrastructure/terraform" 2>/dev/null || true
IAM_ROLE_ARN=$(terraform output -raw aws_load_balancer_controller_role_arn 2>/dev/null || echo "")
cd - > /dev/null

# Check if ServiceAccount already exists (from previous run)
if kubectl get serviceaccount aws-load-balancer-controller -n kube-system &>/dev/null; then
  echo "ServiceAccount already exists. Checking if it's managed by Helm..."
  
  # Check if it has Helm labels (managed by Helm)
  if ! kubectl get serviceaccount aws-load-balancer-controller -n kube-system -o jsonpath='{.metadata.labels.app\.kubernetes\.io/managed-by}' | grep -q Helm; then
    echo "Removing existing ServiceAccount (not managed by Helm)..."
    kubectl delete serviceaccount aws-load-balancer-controller -n kube-system
  fi
fi

# Install AWS Load Balancer Controller using Helm
# Helm will create the ServiceAccount with proper ownership metadata
echo "Installing AWS Load Balancer Controller..."

if [ ! -z "$IAM_ROLE_ARN" ]; then
  echo "Using IAM role from Terraform: $IAM_ROLE_ARN"
  helm upgrade --install aws-load-balancer-controller eks/aws-load-balancer-controller \
    -n kube-system \
    --set clusterName=$CLUSTER_NAME \
    --set region=$AWS_REGION \
    --set serviceAccount.create=true \
    --set serviceAccount.name=aws-load-balancer-controller \
    --set serviceAccount.annotations."eks\.amazonaws\.com/role-arn"=$IAM_ROLE_ARN \
    --wait
else
  echo "Warning: IAM role ARN not found in Terraform output."
  echo "The controller will use the node group role (may have permission issues)."
  echo "Run 'terraform apply' first to create the IAM role, then re-run this script."
  helm upgrade --install aws-load-balancer-controller eks/aws-load-balancer-controller \
    -n kube-system \
    --set clusterName=$CLUSTER_NAME \
    --set region=$AWS_REGION \
    --set serviceAccount.create=true \
    --set serviceAccount.name=aws-load-balancer-controller \
    --wait
fi

echo "AWS Load Balancer Controller installed!"
kubectl get pods -n kube-system | grep aws-load-balancer

